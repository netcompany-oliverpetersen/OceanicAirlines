CREATE TABLE [DATA].[BOOKING]
(
	[Id] INT IDENTITY(1,1) NOT NULL, 
    [StartPosID] INT NOT NULL, 
    [EndPosID] INT NOT NULL, 
    [CustomerID] INT NOT NULL, 
    [UserID] INT NOT NULL, 
    [Timestamp] DATETIME2 NOT NULL 

CONSTRAINT [PK_BOOKING] PRIMARY KEY (Id)

CONSTRAINT [FK_STARTPOS] FOREIGN KEY (StartPosID) REFERENCES DATA.CITY(Id)
CONSTRAINT [FK_ENDPOS] FOREIGN KEY (EndPosID) REFERENCES DATA.CITY(Id)
CONSTRAINT [FK_CUSTOMER] FOREIGN KEY (CustomerID) REFERENCES DATA.CUSTOMER(Id)
CONSTRAINT [FK_USER] FOREIGN KEY (UserID) REFERENCES DATA.[USER](Id)
)
